package objectorientedconcepts;

public class TotalApples {
    public static void main(String[] args) {
        double danielApples = 3.5;
        double amberApples = 2.5;
        
        double totalApples = danielApples + amberApples;
        
        System.out.println("Daniel and Amber have together: " + totalApples + " apples.");
    }
}
